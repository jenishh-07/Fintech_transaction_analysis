-- Q6
-- Merchant Category Revenue Contribution
-- Calculate total revenue generated by each merchant category and rank categories by revenue.

SELECT *
FROM
(
SELECT 
    m.category,
    SUM(t.amount) AS total_revenue,
    DENSE_RANK() OVER(
        ORDER BY SUM(t.amount) DESC
    ) AS rank
