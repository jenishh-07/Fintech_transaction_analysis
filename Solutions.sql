)

SELECT *
FROM
(
SELECT *,
    DENSE_RANK() OVER(
        PARTITION BY category
        ORDER BY total_revenue DESC
    ) AS rank
FROM merchant_revenue
) ranked
WHERE rank = 1;



-- Q4
-- Daily Transaction Leaders
-- Identify the customer generating the highest transaction amount on each day.

WITH daily_spend AS
(
SELECT 
    c.customer_id,
    t.transaction_date,
    SUM(t.amount) AS total_spent
FROM transactions t
JOIN accounts a
    ON t.account_id = a.account_id
JOIN customers c
    ON a.customer_id = c.customer_id
GROUP BY c.customer_id,t.transaction_date
)

SELECT *
FROM
(
SELECT *,
    DENSE_RANK() OVER(
        PARTITION BY transaction_date
        ORDER BY total_spent DESC
    ) AS rank
FROM daily_spend
) ranked
WHERE rank = 1;



