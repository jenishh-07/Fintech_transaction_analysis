-- Q1
-- Top Spending Customer in Each City
-- Identify the highest spending customer in every city based on total transaction amount.

WITH customer_spend AS
(
SELECT 
    c.city,
    c.customer_id,
    SUM(t.amount) AS total_spent
FROM customers c
JOIN accounts a
    ON c.customer_id = a.customer_id
JOIN transactions t
    ON a.account_id = t.account_id
GROUP BY c.city,c.customer_id
)

SELECT *
FROM
(
SELECT *,
    DENSE_RANK() OVER(
        PARTITION BY city
        ORDER BY total_spent DESC
    ) AS rank
FROM customer_spend
) ranked
WHERE rank = 1;



-- Q2
-- Customers Spending Above Their City Average
-- Identify customers whose total spending is greater than the average spending of customers in their city.

WITH city_customer_spend AS
(
SELECT 
    c.city,
    c.customer_id,
    SUM(t.amount) AS total_spent
FROM customers c
JOIN accounts a
    ON c.customer_id = a.customer_id
JOIN transactions t
    ON a.account_id = t.account_id
GROUP BY c.city,c.customer_id
)

SELECT *
FROM city_customer_spend cs
WHERE total_spent >
(
SELECT AVG(total_spent)
FROM city_customer_spend
WHERE city = cs.city
);



-- Q3
-- Top Merchant in Each Category
-- Identify the merchant generating the highest transaction revenue within each merchant category.

WITH merchant_revenue AS
(
SELECT 
    m.category,
    m.merchant_name,
    SUM(t.amount) AS total_revenue
