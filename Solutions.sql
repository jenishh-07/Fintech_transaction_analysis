-- Q7
-- Cities with Highest Average Customer Spending
-- Identify cities with the highest average customer transaction spending.

WITH city_customer_spend AS
(
SELECT 
    c.city,
    c.customer_id,
    SUM(t.amount) AS total_spent
FROM customers c
JOIN accounts a
    ON c.customer_id = a.customer_id
JOIN transactions t
    ON a.account_id = t.account_id
GROUP BY c.city,c.customer_id
)

SELECT 
    city,
    AVG(total_spent) AS avg_customer_spend
FROM city_customer_spend
GROUP BY city
ORDER BY avg_customer_spend DESC;



-- Q8
-- Customers Without Loans but High Spending
-- Identify customers who have high transaction spending but do not currently have loans.

WITH customer_spend AS
(
SELECT 
    c.customer_id,
    SUM(t.amount) AS total_spent
FROM customers c
JOIN accounts a
    ON c.customer_id = a.customer_id
JOIN transactions t
    ON a.account_id = t.account_id
GROUP BY c.customer_id
)

SELECT *
FROM customer_spend cs
LEFT JOIN loans l
    ON cs.customer_id = l.customer_id
WHERE l.loan_id IS NULL
ORDER BY total_spent DESC;



-- Q9
-- Monthly Transaction Growth (MoM)
-- Calculate month-over-month growth in total transaction revenue.

WITH monthly_revenue AS
(
SELECT 
    MONTH(transaction_date) AS month,
    SUM(amount) AS revenue
FROM transactions
GROUP BY MONTH(transaction_date)
)

SELECT 
    month,
    revenue,
    LAG(revenue) OVER(ORDER BY month) AS previous_month,
    ROUND(
        (revenue - LAG(revenue) OVER(ORDER BY month)) /
        LAG(revenue) OVER(ORDER BY month) * 100
        ,2
    ) AS growth_percent
FROM monthly_revenue;



